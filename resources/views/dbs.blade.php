                                        <p><u><strong>FAQs</strong></u></p>
                                        <p><strong>Q: Why do I need to get my documents verified?</strong></p>
                                        <p>A: The DBS check is the highest criminal records check in place and guidelines are very strict. Candidates who are cleared will be allowed to work with vulnerable groups and as such we need to ensure
                                            all documents are legitimate to eliminate fraud.</p>
                                        <p><strong>Q: How long does my certificate take?</strong></p>
                                        <p>A: DBS state it can take up to 6 weeks to receive your certificate, however, the majority of certificates return within 2 weeks. In certain circumstances it could taken longer than 6 weeks. Typically
                                            the longest part of the process is the police records search and this can vary by area.</p>
                                        <p><strong>Q: </strong><strong>When should I repeat a DBS check?</strong></p>
                                        <p>A: According to gov.uk there is no official expiry date for a DBS check. Any information on the check will be up to date at the time the check was carried out however it is up to individual companies
                                            to decide when a new DBS check is needed.</p>
