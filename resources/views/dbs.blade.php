                                        <p>Disclosure and barring services (DBS), previously known as Criminal records Bureau (CRB) are in place to prevent unsuitable people from working with vulnerable groups, including children. As
                                            Optometrists/Opticians you will likely encounter patients from vulnerable groups on a daily basis. Therefore you are required to have an enhanced criminal record check completed.</p>
                                        <p>An enhanced DBS check is the highest level of check available. Most roles within the healthcare industry require an enhanced DBS. At this level they will check cautions, warnings, reprimands, and
                                            convictions, as well as checking if the candidate is barred from working with children or vulnerable adults. It is a criminal offence for an Optometrist or Optician to work with these vulnerable
                                            groups if they are on the barred list.</p>
